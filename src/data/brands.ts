// Helper function to get featured brands
export function getFeaturedBrands(): Brand[] {
  return BRANDS.filter(brand => brand.featured).sort((a, b) => 
    (b.stats?.popularityScore || 0) - (a.stats?.popularityScore || 0)
  );
}

// Helper function to get brands by category
export function getBrandsByCategory(category: string): Brand[] {
  return BRANDS.filter(brand => brand.category === category);
}
